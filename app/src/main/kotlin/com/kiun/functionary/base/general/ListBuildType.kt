package com.kiun.functionary.base.general

/**
 * 表单类型
 */
enum class FormType{

    /**
     * 文本类型
     */
    Text,

    /**
     * 数字
     */
    Number,

    /**
     * 布尔类型
     */
    Bool,

    /**
     * 枚举
     */
    Enum,

    /**
     * 日期
     */
    Date,

    /**
     * 时间
     */
    Time,

    /**
     * 日期和时间
     */
    DateAndTime,

    /**
     * 单行代码
     */
    CodeLine,

    /**
     * 多行代码
     */
    CodeMultiline,

    /**
     * 隐藏
     */
    Hidden,

    /**
     * 自动
     */
    Auto
}
